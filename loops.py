# For Loop 10 program

# print 1 to 10 series
for i in range(1,11) :
    print(i , end=" ")
print()

# print 1 to 10 in 2 differance
for j in range(1,11,2) :
    print(j)
print()


# print 10 to 1 
for c  in range(11,0,-1) :
    print(c)
print()


# Print 2's Table
for s in range(1,11):
    print(s*2, end=" ")
print()


# print odd number
for o in range(1,30) :
    if o % 2 !=0 :
        print(o)


# pyramid
for p in range(1,6) :
    print("*" * p)
print()


# reverse pyramid
for r in range(5,0,-1) :
    print("*" * r)




